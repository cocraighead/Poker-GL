   canvas.resize = function ()
   {
      var size = Math.min(window.innerWidth, window.innerHeight)-10;
      canvas.width  = canvas.width  = window.innerWidth - 210;
      canvas.height = canvas.height = window.innerHeight - 10;
      var xStart =  (window.innerWidth - 210 - size)/2;
      gl.viewport(xStart,0,size,size);
      requestAnimationFrame(Display);
   }

   //
   //  Mouse button pressed
   //
   canvas.onmousedown = function (ev)
   {
      move  = 1;
      x0 = ev.clientX;
      y0 = ev.clientY;
   }

   //
   //  Mouse button released
   //
   canvas.onmouseup = function (ev)
   {
      move  = 0;
   }

   //
   //  Mouse movement
   //
   canvas.onmousemove = function (ev)
   {
      if (move==0) return;
      //  Update angles
      ph += (ev.clientY-y0)/100;
      th += (ev.clientX-x0)/100;
      //  Store location
      x0 = ev.clientX;
      y0 = ev.clientY;
      //  Redisplay
      requestAnimationFrame(Display);
   }
